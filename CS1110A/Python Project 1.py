S = input('Enter less than 50 characters:')

# Test for alphanumeric characters
print('S has alphanumeric characters:', S.isalnum())

# Test for alphabetical characters
print('S has alphabetical characters:', S.isalpha())

# Test for digits
print('S has digits:', S.isdigit())

# Test for lowercase characters
print('S has lowercase characters:', S.islower())

# Test for uppercase characters
print('S has uppercase characters:', S.isupper())